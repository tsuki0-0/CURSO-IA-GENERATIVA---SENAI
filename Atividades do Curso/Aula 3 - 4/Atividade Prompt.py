# Prompt CRAFT
# Estrutura com Exemplo:
# Contexto: Uma pequena empresa quer melhorar suas vendas online.
# Role: Você é especialista em marketing digital.
# Action: Explique estratégias de marketing digital.
# Format: Apresente 5 estratégias em lista. formule em Markdown
# Target: Empreendedores iniciantes.

# Prompt COSTAR
# Estrutura com Exemplo:
# Contexto: Uma startup deseja crescer nas redes sociais.
# Objetivo: Criar estratégias de marketing digital.
# Estilo: Didático.
# Tom: Profissional.
# Audiência: Empreendedores iniciantes.
# Resposta: Liste 5 estratégias e explique cada uma.

